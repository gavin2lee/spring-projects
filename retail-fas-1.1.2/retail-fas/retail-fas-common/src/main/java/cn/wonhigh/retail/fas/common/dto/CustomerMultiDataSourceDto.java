package cn.wonhigh.retail.fas.common.dto;

/**
 * 客户多数据源组装的对象
 * 
 * @author yang.y
 *
 */
public class CustomerMultiDataSourceDto {

	/** 编码 */
	private String code;
	
	/** 名称 */
	private String name;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
