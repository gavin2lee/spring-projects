package com.lachesis.mnis.core.inoutmanager.entity;

public class InOutManagerStatistic {
	/**
	 * 总入量
	 */
	private double inTotalCount;
	/**
	 * 总出量
	 */
	private double outTotalCount;
	public double getInTotalCount() {
		return inTotalCount;
	}
	public void setInTotalCount(double inTotalCount) {
		this.inTotalCount = inTotalCount;
	}
	public double getOutTotalCount() {
		return outTotalCount;
	}
	public void setOutTotalCount(double outTotalCount) {
		this.outTotalCount = outTotalCount;
	}
}
