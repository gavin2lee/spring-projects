package com.anyi.report.entity;

public class PrintModelCheckBoxText {

	private String text_name;//输入框显示的名字
	private String text_unit;//输入框的单位
	private String text_value;//输入框填写的值
	private String checkbox_text_id;
	public String getText_name() {
		return text_name;
	}
	public void setText_name(String text_name) {
		this.text_name = text_name;
	}
	public String getText_unit() {
		return text_unit;
	}
	public void setText_unit(String text_unit) {
		this.text_unit = text_unit;
	}
	public String getCheckbox_text_id() {
		return checkbox_text_id;
	}
	public void setCheckbox_text_id(String checkbox_text_id) {
		this.checkbox_text_id = checkbox_text_id;
	}
	public String getText_value() {
		return text_value;
	}
	public void setText_value(String text_value) {
		this.text_value = text_value;
	}
	
	
	
}
