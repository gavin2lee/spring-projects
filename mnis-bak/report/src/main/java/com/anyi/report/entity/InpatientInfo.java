/**
 * 病人基本信息键值组，如name:cyc
 * @author yanchang.chen
 *
 */
public class InpatientInfo {	
	
	private String key;
	private String value;
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}
