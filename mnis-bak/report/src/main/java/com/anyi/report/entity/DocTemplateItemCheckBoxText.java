package com.anyi.report.entity;

public class DocTemplateItemCheckBoxText {

	private String text_name;
	private String text_unit;
	private String text_data_type;
	private String checkbox_text_ord;
	private String checkbox_text_code;
	private String width;
	public String getText_name() {
		return text_name;
	}
	public void setText_name(String text_name) {
		this.text_name = text_name;
	}
	public String getText_unit() {
		return text_unit;
	}
	public void setText_unit(String text_unit) {
		this.text_unit = text_unit;
	}
	public String getText_data_type() {
		return text_data_type;
	}
	public void setText_data_type(String text_data_type) {
		this.text_data_type = text_data_type;
	}
	public String getCheckbox_text_ord() {
		return checkbox_text_ord;
	}
	public void setCheckbox_text_ord(String checkbox_text_ord) {
		this.checkbox_text_ord = checkbox_text_ord;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String getCheckbox_text_code() {
		return checkbox_text_code;
	}
	public void setCheckbox_text_code(String checkbox_text_code) {
		this.checkbox_text_code = checkbox_text_code;
	}
	
}
