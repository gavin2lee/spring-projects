package com.anyi.report.entity;

public class ModelForTreeDataList {

	private String recordId;
	private String createTime;
	private String template_id;
	private String inpatient_no;
	private String data_index;
	public String getRecordId() {
		return recordId;
	}
	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getTemplate_id() {
		return template_id;
	}
	public void setTemplate_id(String template_id) {
		this.template_id = template_id;
	}
	public String getInpatient_no() {
		return inpatient_no;
	}
	public void setInpatient_no(String inpatient_no) {
		this.inpatient_no = inpatient_no;
	}
	public String getData_index() {
		return data_index;
	}
	public void setData_index(String data_index) {
		this.data_index = data_index;
	}
	
}
